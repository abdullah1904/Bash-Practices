# Arithmetic Operators
num1=20
num2=10
echo $((num1+num2))
echo $((num1-num2))
echo $((num1*num2))
echo $((num1/num2))
echo $((num1%num2))
echo $((num1++))
echo $((++num2))
echo $((num2--))
echo $((--num1))

